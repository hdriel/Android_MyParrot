package myparrot.diseases;


public class Child {

    private String copy, descreption, simptom;
    
    
	public Child(String copy, String descreption, String simptom) {
		this.copy = copy;
		this.descreption = descreption;
		this.simptom = simptom;
		
	}



	public String getCopy() {
		return copy;
	}



	public void setCopy(String copy) {
		this.copy = copy;
	}



	public String getDescreption() {
		return descreption;
	}



	public void setDescreption(String descreption) {
		this.descreption = descreption;
	}



	public String getSimptom() {
		return simptom;
	}



	public void setSimptom(String simptom) {
		this.simptom = simptom;
	}



	
	
    
}